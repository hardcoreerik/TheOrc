    /// <summary>
    /// Run one capability test against the given model.
    /// Uses a temporary workspace that is cleaned up after the test.
    /// </summary>
    public static async Task<ModelCapabilityTestResult> RunAsync(
        string modelId,
        string testId,
        string ollamaHost,
        IProgress<string>? progress = null,
        CancellationToken ct = default)
    {
        var test = testId switch
        {
            "FileWriteSmall"  => SmallTest,
            "FileWriteMedium" => MediumTest,
            "FileWriteLarge"  => LargeTest,
            _ => throw new ArgumentException($"Unknown test ID: {testId}"),
        };

        var tempDir = Path.Combine(
            Path.GetTempPath(), "TheOrc", "ModelTests",
            DateTime.Now.ToString("yyyyMMdd_HHmmss") + "_" + modelId.Replace(":", "_").Replace("/", "_"));

        Directory.CreateDirectory(tempDir);
        progress?.Report($"Temp workspace: {tempDir}");

        var result = new ModelCapabilityTestResult
        {
            ModelId   = modelId,
            TestId    = testId,
            TestName  = test.TestName,
            Timestamp = DateTime.UtcNow,
            ExpectedFile = test.ExpectedFileName,
        };

        try
        {
            progress?.Report($"[PHASE:sending] Sending prompt to {modelId}…");
            var sw = System.Diagnostics.Stopwatch.StartNew();
            var rawResponse = await CallOllamaAsync(ollamaHost, modelId, test.PromptText, ct);
            sw.Stop();
            progress?.Report($"[PHASE:received:{rawResponse.Length}] Response received — {rawResponse.Length:N0} chars in {sw.Elapsed.TotalSeconds:F1}s.");

            // Analyse the raw response JSON for write_file tool calls
            progress?.Report("[PHASE:analyzing] Analyzing response…");
            AnalyseResponse(rawResponse, test, tempDir, result, progress);

            // Try to write the file if we found valid content
            if (result.ValidJson && !string.IsNullOrEmpty(_lastExtractedContent))
            {
                var filePath = Path.Combine(tempDir, test.ExpectedFileName);
                await File.WriteAllTextAsync(filePath, _lastExtractedContent, ct);
                result.FileWritten      = File.Exists(filePath);
                result.ActualFileSizeBytes = result.FileWritten
                    ? (int)new FileInfo(filePath).Length : 0;
                progress?.Report(result.FileWritten
                    ? $"✅ File written: {test.ExpectedFileName} ({result.ActualFileSizeBytes} bytes)"
                    : "❌ File not written.");
            }

            // Determine overall result
            result.Result = result.ValidJson && result.FileWritten ? "pass"
                          : result.ValidJson && !result.FileWritten ? "partial"
                          : "fail";

            progress?.Report($"[RESULT:{result.Result}] {result.Result.ToUpperInvariant()}");
        }
        catch (OperationCanceledException) { result.Result = "fail"; result.Notes = "Cancelled."; }
        catch (Exception ex)               { result.Result = "fail"; result.Notes = $"Error: {ex.Message}"; }
        finally
        {
            // Best-effort cleanup of temp dir
            try { Directory.Delete(tempDir, true); } catch { /* non-fatal */ }
        }

        await ModelWikiService.SaveUserResultAsync(result);
        return result;
    }

    // ── Private helpers ───────────────────────────────────────────────────────

    [ThreadStatic] private static string? _lastExtractedContent;

    private static async Task<string> CallOllamaAsync(
        string host, string modelId, string prompt, CancellationToken ct)
    {
        var writeFileTool = JsonNode.Parse("""
            {
              "type": "function",
              "function": {
                "name": "write_file",
                "description": "Write content to a file at the given path",
                "parameters": {
                  "type": "object",
                  "properties": {
                    "path":    { "type": "string", "description": "File path to write" },
                    "content": { "type": "string", "description": "File content"       }
                  },
                  "required": ["path", "content"]
                }
              }
            }
            """)!;

        var request = new JsonObject
        {
            ["model"]    = modelId,
            ["stream"]   = false,
            ["messages"] = new JsonArray
            {
                new JsonObject
                {
                    ["role"]    = "user",
                    ["content"] = prompt,
                }
            },
            ["tools"] = new JsonArray { writeFileTool },
        };

        var url     = $"{host.TrimEnd('/')}/v1/chat/completions";
        var content = new StringContent(request.ToJsonString(),
            Encoding.UTF8, "application/json");

        var resp = await _http.PostAsync(url, content, ct);
        resp.EnsureSuccessStatusCode();
        return await resp.Content.ReadAsStringAsync(ct);
    }

    private static void AnalyseResponse(
        string rawResponse,
        TestDefinition test,
        string tempDir,
        ModelCapabilityTestResult result,
        IProgress<string>? progress)
    {
        _lastExtractedContent = null;

        try
        {
            var doc = JsonDocument.Parse(rawResponse);
            var root = doc.RootElement;

            // Try native tool_calls first
            if (root.TryGetProperty("choices", out var choices) &&
                choices.GetArrayLength() > 0)
            {
                var choice  = choices[0];
                var message = choice.GetProperty("message");

                if (message.TryGetProperty("tool_calls", out var toolCalls) &&
                    toolCalls.GetArrayLength() > 0)
                {
                    var tc   = toolCalls[0];
                    var func = tc.GetProperty("function");
                    var name = func.GetProperty("name").GetString();

                    if (name == "write_file")
                    {
                        var argsRaw = func.GetProperty("arguments").GetString() ?? "{}";
                        var opens   = argsRaw.Count(c => c == '{');
                        var closes  = argsRaw.Count(c => c == '}');
                        result.OpenBraceCount  = opens;
                        result.CloseBraceCount = closes;
                        result.Truncated       = opens > closes;

                        if (!result.Truncated)
                        {
                            var args    = JsonDocument.Parse(argsRaw).RootElement;
                            var content = args.TryGetProperty("content", out var cv)
                                        ? cv.GetString() : null;
                            result.ValidJson = true;
                            _lastExtractedContent = content;
                            progress?.Report($"Native tool call found. JSON valid. Content length: {content?.Length ?? 0}");
                        }
                        else
                        {
                            progress?.Report($"Native tool call found but JSON TRUNCATED (opens={opens} closes={closes}).");
                            result.ValidJson = false;
                            result.Notes = $"write_file JSON truncated at {argsRaw.Length} chars (opens={opens}, closes={closes})";
                        }
                        return;
                    }
                }

                // Try text response containing JSON tool call (text-JSON format)
                if (message.TryGetProperty("content", out var textContent))
                {
                    var text   = textContent.GetString() ?? "";
                    var opens  = text.Count(c => c == '{');
                    var closes = text.Count(c => c == '}');
                    result.OpenBraceCount  = opens;
                    result.CloseBraceCount = closes;
                    progress?.Report($"Text response ({text.Length} chars, opens={opens} closes={closes}).");

                    // Look for write_file JSON block in text
                    var nameIdx = text.IndexOf("\"write_file\"", StringComparison.OrdinalIgnoreCase);
                    if (nameIdx >= 0 || text.Contains("write_file"))
                    {
                        result.Truncated = opens > closes;
                        if (!result.Truncated && opens > 0)
                        {
                            // Try to extract content field
                            var contentMatch = System.Text.RegularExpressions.Regex.Match(
                                text, @"""content""\s*:\s*""((?:[^""\\]|\\.)*)""");
                            if (contentMatch.Success)
                            {
                                _lastExtractedContent = contentMatch.Groups[1].Value
                                    .Replace("\\n", "\n").Replace("\\\"", "\"").Replace("\\\\", "\\");
                                result.ValidJson = true;
                                progress?.Report($"Text-JSON write_file found. Content length: {_lastExtractedContent.Length}");
                            }
                        }
                        else if (result.Truncated)
                        {
                            result.Notes = $"Text-JSON write_file truncated ({text.Length} chars, opens={opens}, closes={closes})";
                        }
                    }
                    else if (text.Length == 0)
                    {
                        result.Notes = "Empty response from model.";
                    }
                    else
                    {
                        result.Notes = $"No write_file call found in response ({text.Length} chars).";
                    }
                }
            }
        }
        catch (JsonException ex)
        {
            result.Notes = $"Failed to parse Ollama response: {ex.Message}";
        }
    }
