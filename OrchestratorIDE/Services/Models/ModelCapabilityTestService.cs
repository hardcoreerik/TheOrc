/// <summary>
/// Runs lightweight capability tests against a local Ollama model
/// to measure write_file JSON generation reliability at different payload sizes.
///
/// Tests:
///   FileWriteSmall  — write_file hello.txt with 1 line  (~20 chars)
///   FileWriteMedium — write_file script.py with ~50 lines (~1.5 KB)
///   FileWriteLarge  — write_file app.py with ~150 lines  (~5 KB)
///
/// Results are saved to %APPDATA%\OrchestratorIDE\model-wiki\results.jsonl.
///
/// Does NOT use AgentLoop — makes direct /v1/chat/completions requests with
/// stream:false so results are self-contained and don't affect agent state.
///
/// Does NOT write to the user's active project workspace.
/// Uses %TEMP%\TheOrc\ModelTests\{timestamp}\ as an isolated temp workspace.
/// </summary>
public static class ModelCapabilityTestService
{
    private static readonly HttpClient _http = new() { Timeout = TimeSpan.FromSeconds(120) };

